public class SpecialCharacters {
    public static void main(String[] args)
    {
        //Backspace
        System.out.println("Divergent softwareh\b ");

        
        //Tab
        System.out.println("Divergent\t \u0009  software");


        //Carriagereturn
        System.out.println("Divergent\r software");


        //Enter
        System.out.println("Divergent\n software");


        //Double quotes
        System.out.println("\"Divergent software\"");


        //Backslash
        System.out.println("\\Divergent software\\");

    }

    
}
